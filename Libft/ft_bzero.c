#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	int		*buf;

	i = 0;
	buf = (int *)s;
	while (i < n)
	{
		buf[i] = 0;
		i++;
	}
	s = (void *)buf;
}
