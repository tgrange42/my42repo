int		ft_atoi(const char *str)
{
	int		res;
	int		i;
	int		neg;

	neg = 0;
	i = 0;
	res = 0;
	while (str[i] < 33)
		i++;
	if (str[i] == '-')
	{
		neg = 1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] <= '9' && str[i] >= '0')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (neg == 1)
		return (-res);
	return (res);
}
