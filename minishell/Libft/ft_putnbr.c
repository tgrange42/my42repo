#include "libft.h"

void	ft_putnbr(int n)
{
	int		x;

	x = 1000000000;
	if (n == 0)
	{
		ft_putchar('0');
		return ;
	}
	while (n / x == 0)
		x /= 10;
	if (n < 0)
		ft_putchar('-');
	else
		n *= -1;
	while (x)
	{
		ft_putchar(-(n / x) + '0');
		n %= x;
		x /= 10;
	}
}
